function interpolate(template, vars = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ''));
}

export function t(language = 'en', key, vars = {}) {
  const locale = uiTranslations[language] || {};
  const runtimeLocale = runtimeUiTranslations[language] || {};
  const template = locale[key] || runtimeLocale[key] || uiTranslations.en[key] || key;
  return interpolate(template, vars);
}

export function getLanguageLabel(language = 'en') {
  return LANGUAGE_OPTIONS.find((option) => option.id === language)?.label || 'English';
}

export function getUiTranslationEntries() {
  return Object.entries(uiTranslations.en)
    .filter(([key, value]) => typeof value === 'string' && !UI_TRANSLATION_EXCLUDED_KEYS.has(key))
    .map(([key, text]) => ({ key, text }));
}

export function getLabelTranslationEntries() {
  return [...new Set(LABEL_TRANSLATION_ENTRIES)];
}

export function registerUiTranslations(language = 'en', translations = {}) {
  if (!language || language === 'en') return;

  runtimeUiTranslations[language] = {
    ...(runtimeUiTranslations[language] || {}),
    ...translations
  };
}

export function registerLabelTranslations(language = 'en', translations = {}) {
  if (!language || language === 'en') return;

  runtimeLabelTranslations[language] = {
    ...(runtimeLabelTranslations[language] || {}),
    ...translations
  };
}

export function translateLabel(language = 'en', value = '') {
  if (!value || language === 'en') return value;
  return labelTranslations[language]?.[value] || runtimeLabelTranslations[language]?.[value] || value;
}

export function sanitizeRenderedText(value = '') {
  return String(value || '')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<\/?[^>]+>/g, ' ')
    .replace(/<(?:\/|[a-zA-Z!])[^>\n\r]*/g, ' ')
    .replace(/&nbsp;/gi, ' ')
    .replace(/&amp;/gi, '&')
    .replace(/&quot;/gi, '"')
    .replace(/&#39;|&apos;/gi, '\'')
    .replace(/&lt;/gi, '<')
    .replace(/&gt;/gi, '>')
    .replace(/\s+/g, ' ')
    .trim();
}

export function translateCachedText(language = 'en', cache = {}, value = '') {
  if (!value) return value;
  const normalizedValue = typeof value === 'string' ? value.trim() : value;
  if (language === 'en') {
    return sanitizeRenderedText(normalizedValue);
  }

  return sanitizeRenderedText(cache[value] || cache[normalizedValue] || translateLabel(language, normalizedValue) || value);
}

export function translateCachedList(language = 'en', cache = {}, values = []) {
  return (values || []).map((value) => translateCachedText(language, cache, value));
}

export function collectEventTexts(events = []) {
  return events.flatMap((event) => [
    event?.title,
    event?.summary,
    event?.region,
    event?.category_label,
    event?.what_happened,
    event?.why_it_matters,
    event?.suggested_action,
    event?.source,
    ...(event?.market_impact || []),
    ...(event?.personal_finance_impact || []),
    ...(event?.possible_outcomes || []),
    event?.prediction?.short_term,
    event?.prediction?.long_term,
    event?.prediction?.confidence
  ]).filter(Boolean);
}
